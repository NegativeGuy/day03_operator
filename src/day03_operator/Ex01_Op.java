package day03_operator;

public class Ex01_Op {
	public static void main(String[] args) {
		/*
		 [연산자]
		  /(나누기)	: 정수 / 정수 = 정수(몫)
		    		  실수 / 정수 = 실수
		  
		  %(mod)	: 정수 % 정수 = 나머지 값 
		 
		 	예) 10 / 3	 = 3(정수 몫만 출력)
		 	    10 / 3.0 = 3.3333...(실수 값으로 나옴)
		 	    10 % 3   = 1 (나머지 값)
		 	    
		 	    배수 구하기
		 	    x % 2 = 0(나머지값) : x는 2의 배수 (혹은 짝수)
		 	    x % 3 = 0(나머지값) : x는 3의 배수
		 */
	}
}
